/**
 * Compiles the GraphStateAnnotation topology into an executable parallel execution graph.[span_67](start_span)[span_67](end_span)[span_68](start_span)[span_68](end_span)
 */
export function buildEngineWorkflow() {
  const graph = new StateGraph(GraphStateAnnotation)
   .addNode("style_auditor", styleAuditorNode)
   .addNode("security_scanner", securityScannerNode)
   .addNode("logic_reviewer", logicReviewerNode)
   .addNode("aggregator", aggregatorNode)
    // Establish parallel fan-out
   .addEdge(START, "style_auditor")
   .addEdge(START, "security_scanner")
   .addEdge(START, "logic_reviewer")
    // Establish parallel fan-in to aggregator node
   .addEdge("style_auditor", "aggregator")
   .addEdge("security_scanner", "aggregator")
   .addEdge("logic_reviewer", "aggregator")
   .addEdge("aggregator", END);

  return graph.compile();
}
