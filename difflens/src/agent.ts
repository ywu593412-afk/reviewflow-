// 全局一键切换开关
const CURRENT_PROVIDER = "gemini"; 
const CURRENT_MODEL = "gemini-1.5-pro";    

function getModelInstance(temperature: number) {
  const config = {
    temperature: temperature,
  };

  switch (CURRENT_PROVIDER) {
    case "openai":
      return new ChatOpenAI({
        ...config,
        modelName: CURRENT_MODEL,
        openAIApiKey: process.env.OPENAI_API_KEY,
      });
    case "deepseek":
      return new ChatOpenAI({
        ...config,
        modelName: CURRENT_MODEL,
        openAIApiKey: process.env.DEEPSEEK_API_KEY,
        configuration: { baseURL: "https://api.deepseek.com/v1" },
      });
    case "gemini":
      return new ChatGoogleGenAI({
        ...config,
        modelName: CURRENT_MODEL,
        apiKey: process.env.GEMINI_API_KEY,
      });
    case "claude":
      return new ChatAnthropic({
        ...config,
        modelName: CURRENT_MODEL,
        apiKey: process.env.ANTHROPIC_API_KEY,
      });
    default:
      throw new Error(`Unsupported provider: ${CURRENT_PROVIDER}`);
  }
}
  
const CommentSchema = z.object({
  path: z.string().describe("Relative file path being audited (e.g., 'src/utils.ts')."),
  line: z.number().describe("Precise relative line number on the right side of the diff."),
  body: z.string().describe("The actual detailed review content formatted in markdown. Include standard-compliant code fixes."),
});

const MultiAgentReviewResultSchema = z.object({
  comments: z.array(CommentSchema).describe("Set of code quality comments generated by the auditor."),
});

type OutputSchemaType = z.infer<typeof MultiAgentReviewResultSchema>;

/**
 * Executes a specialized domain review on raw code diffs using GPT-4o.
 */
async function executeAuditor(
  prompt: string,
  diff: string,
  temperature: number
): Promise<ReviewComment> {
const apiKey = CURRENT_PROVIDER === "deepseek" ? process.env.DEEPSEEK_API_KEY : process.env.OPENAI_API_KEY;
const baseURL = CURRENT_PROVIDER === "deepseek" ? "https://api.deepseek.com/v1" : undefined;

const model = new ChatOpenAI({
  modelName: CURRENT_MODEL,
  temperature: temperature,
  openAIApiKey: apiKey,
  configuration: baseURL ? { baseURL } : undefined
}).withStructuredOutput(MultiAgentReviewResultSchema);


  try {
    const rawResponse = await model.invoke();
    const response = rawResponse as OutputSchemaType;
    return response.comments;
  } catch (error) {
    console.error(`Auditor execution error occurred: ${(error as Error).message}`);
    return;
  }
}

